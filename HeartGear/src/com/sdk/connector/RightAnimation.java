    public static void update() {

        int sampledValue = Integer.parseInt( Protocol.getInstance().getRow()[3].toString());
        int bc = 0;
        if(sampledValue > 0){
            bc = Math.round(60000/sampledValue);
        }
        
        if(Protocol.getInstance().getRightAccumulator().size() > 10){
            jLabel1.setText(java.util.ResourceBundle.getBundle("com/sdk/connector/Bundle").getString("BC") +bc+ java.util.ResourceBundle.getBundle("com/sdk/connector/Bundle").getString("RR")+sampledValue );
        }else{
             jLabel1.setText(java.util.ResourceBundle.getBundle("com/sdk/connector/Bundle").getString("CALIBRATING...") );
        }

    }

 
